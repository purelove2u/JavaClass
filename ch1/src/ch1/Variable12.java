package ch1;

public class Variable12 {

	public static void main(String[] args) {
		// 타입 : int, float, double, boolean, char
		
		// 문자열 : 쌍따옴표 안에 넣기
		String str = "Hello";
		String str2 = "Hello world";

		// 대문자로 시작하는 타입은 객체 타입(class type)
		// 소문자로 시작하는 타입은 기본 타입
		
		System.out.println(str + str2);
		
			
		

	}

}
