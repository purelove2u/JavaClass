package ch3;

public class ForTest1 {

	public static void main(String[] args) {
		// 반복문
		// Hello World!! 10번 출력
		for(int i = 0 ; i < 10 ; i++) {
			System.out.println("Hello World!");
		}
		
		for(int i = 0 ; i <= 10 ; i++) {
			System.out.println(i);
		}
		
		for(int i = 10 ; i >= 1 ; i--) {
			System.out.println(i);
		}
	}

}
