package ch2;

public class AssignmentOpEx1 {
	public static void main(String[] args) {
		// 복합 대입 연산자
		// +=, -=, *=, /=, %=
		
		int result = 10;
		
		result += 10;	//result = result + 10
		System.out.println(result);

		result -= 10;	//result = result - 10
		System.out.println(result);
		
		result *= 3;	//result = result * 3
		System.out.println(result);
		
		result /= 5;	//result = result / 5
		System.out.println(result);
		
		result %= 3;	//result = result % 3
		System.out.println(result);
		
	}
}
